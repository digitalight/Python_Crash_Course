# Sort
cars = ['honda', 'jaguar', 'ford', 'toyota', 'audi']
cars.sort()
print(cars)
cars.sort(reverse=True)
print(cars)

# Sorted
cars = ['honda', 'jaguar', 'ford', 'toyota', 'audi']
print(sorted(cars))

# Reverse
cars = ['honda', 'jaguar', 'ford', 'toyota', 'audi']
cars.reverse()
print(cars)
print(len(cars))